import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.5568628311, green: 0, blue: 6.016343868e-09, alpha: 1)
    }
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.9254902005, green: 0.9254902005, blue: 0.9254903197, alpha: 1)
    }
    
    static func buttonDark() -> UIColor {
        return #colorLiteral(red: 0.1764706075, green: 0.1764705777, blue: 0.1764706075, alpha: 1)
    }
    
    static func textFieldLight() -> UIColor {
        return #colorLiteral(red: 0.8862746358, green: 0.8862745166, blue: 0.8862746358, alpha: 1)
    }
}
